// Incremento e Decremento

let valor1 = 100

// pós incremento
console.log(valor1++);  // 100
console.log(valor1);    // 101

console.log(valor1--);  // 100
console.log(valor1);    // 99

// pré incremento
console.log(++valor1);  // 101
console.log(valor1);    // 101

console.log(--valor1);  // 99
console.log(valor1);    // 99